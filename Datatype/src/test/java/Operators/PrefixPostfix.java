package Operators;

public class PrefixPostfix {
	 public static void main(String[] args) {
		    int numA = 5;
		    int numB = 10;
		    int numC = 0;

		    numC = ++numA + numB;

		    System.out.println(numA);
		    System.out.println(numC);
		    
		    
		    int count = 10;
		    ++count;      
		    --count;      
		    
		    System.out.println(count);
		  }
		  }
		

